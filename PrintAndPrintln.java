
public class PrintAndPrintln {

	public static void main(String[] args) {
		// Using println() prints output and moves to the next line.
		System.out.println("Welcome to Sunrise Cafe:");
		System.out.println("1. Pizza");
		System.out.println("2. Burger");
		System.out.println("3. Pasta");
		
		System.out.println(" ");
		
		
		//Using print() prints output on the same line
		System.out.print("Welcome to Sunrise Cafe: ");
		System.out.print("1. Pizza ");
		System.out.print("2. Burger ");
		System.out.print("3. Pasta");
	}

}
